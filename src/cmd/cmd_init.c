static int mkdir_p(const char *path) {
  char tmp[1024];
  strncpy(tmp, path, sizeof(tmp) - 1);
  tmp[sizeof(tmp) - 1] = '\0';
  size_t len = strlen(tmp);
  if (len > 0 && tmp[len - 1] == '/')
    tmp[len - 1] = '\0';

  for (char *p = tmp + 1; *p; p++) {
    if (*p == '/') {
      *p = '\0';
      mkdir(tmp, 0755);
      *p = '/';
    }
  }
  return mkdir(tmp, 0755);
}

/* Write a manifest.xml wrapper that includes the real manifest */
static int write_manifest_link(const char *repodir, const char *manifest_name) {
  char path[1500];
  snprintf(path, sizeof(path), "%s/manifest.xml", repodir);

  FILE *fp = fopen(path, "w");
  if (!fp) {
    fprintf(stderr, "rpp init: cannot write %s: %s\n", path, strerror(errno));
    return -1;
  }
  fprintf(fp,
          "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n"
          "<!--\n"
          "  DO NOT EDIT THIS FILE!  It is generated by repo and changes will "
          "be discarded.\n"
          "  Use rpp init -m <file> to choose a different manifest.\n"
          "-->\n"
          "<manifest>\n"
          "  <include name=\"%s\" />\n"
          "</manifest>\n",
          manifest_name);
  fclose(fp);
  return 0;
}

